var a=20;
var b=30;

console.log('Arithmatic Operators');
console.log(a,'+',b,'=',a+b);
console.log(a,'-',b,'=',a-b);
console.log(a,'*',b,'=',a*b);
console.log(a,'/',b,'=',a/b);

console.log('Comparision Operators');
console.log(a,'<',b,'=',a<b);
console.log(a,'>',b,'=',a>b);
console.log(a,'<=',b,'=',a<=b);
console.log(a,'>=',b,'=',a>=b);
console.log(a,'!=',b,'=',a!=b);
console.log(a,'==',b,'=',a==b);

console.log('Identify Operators');
console.log(a,'===',b,'--',a===b);
console.log(a,'!==',b,'--',a!==b);






